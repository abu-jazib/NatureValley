  const blogTitle = params.title; // Get the title from the URL  
  const shareUrl = encodeURIComponent(window.location.href); // URL of the current page  

  // Construct the Facebook share URL  
  const facebookShareUrl = `https://www.facebook.com/sharer/sharer.php?u=${shareUrl}&title=${blogTitle}`;  

